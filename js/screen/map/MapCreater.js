    calcSx(mapAdress) {
        return (mapAdress % this.#COLUMN) * this.#CROPSIZE;
    }

    calcSy(mapAdress) {
        const quotient = Math.floor((mapAdress / this.#LINE));
        return quotient * this.#CROPSIZE;
    }

    //  マップの描画処理
    drawMap(context, posX, posY) {
        for (let y = 0; y < this.mapData.length; y++) {
            for (let x = 0; x < this.mapData[y].length; x++) {
                this.drawTile(x * this.#TILESIZE - posX, y * this.#TILESIZE - posY, this.mapData[y][x], context);
            }    
        }
    };

